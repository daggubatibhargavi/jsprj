function displayMenuItems(category, items) {
  let cart = JSON.parse(localStorage.getItem("cart")) || {};
  const container = document.getElementById("menuContainer");
  container.innerHTML = "";

  items.forEach((item) => {
    const itemCard = document.createElement("div");
    itemCard.style.marginTop = "30px";

    const itemImage = document.createElement("img");
    itemImage.src = item.Image;
    itemImage.style.width = "250px";
    itemImage.style.height = "250px";

    const itemName = document.createElement("h3");
    itemName.textContent = item.Name;
    itemName.style.fontSize = "20px";

    const itemPrice = document.createElement("p");
    itemPrice.textContent = `Price: ${item.price}`;

    const quantityDisplay = document.createElement("span");
    quantityDisplay.textContent = cart[item.id]?.quantity || 1;
    quantityDisplay.style.margin = "0 10px";

    const addButton = document.createElement("button");
    addButton.textContent = "+";
    addButton.addEventListener("click", () => {
      if (!cart[item.id]) {
        cart[item.id] = { ...item, quantity: 1 };
      }
      cart[item.id].quantity++;
      quantityDisplay.textContent = cart[item.id].quantity;
      console.log(cart[item.id].quantity);

      localStorage.setItem("cart", JSON.stringify(cart));
      // updateCartIconCount();
    });

    const removeButton = document.createElement("button");
    removeButton.textContent = "-";
    removeButton.addEventListener("click", () => {
      if (cart[item.id]) {
        cart[item.id].quantity--;
        if (cart[item.id].quantity < 1) {
          delete cart[item.id];
          quantityDisplay.textContent = "0";
        } else {
          quantityDisplay.textContent = cart[item.id].quantity;
        }
        localStorage.setItem("cart", JSON.stringify(cart));
        // updateCartIconCount();
      }
    });

    const buyNowButton = document.createElement("button");
    buyNowButton.textContent = "Buy Now";
    buyNowButton.style.marginLeft = "70px";
    buyNowButton.addEventListener("click", () => {
      window.location.href = "./order.html";
    });

    container.style.cssText = `
      display: flex;
      flex-wrap: wrap;
      gap: 60px;
      width: 100%;
      margin: 100px auto;
      height: auto;
    `;

    itemCard.appendChild(itemImage);
    itemCard.appendChild(itemName);
    itemCard.appendChild(itemPrice);
    itemCard.appendChild(removeButton);
    itemCard.appendChild(quantityDisplay);
    itemCard.appendChild(addButton);
    itemCard.appendChild(buyNowButton);

    container.appendChild(itemCard);
  });
}

function bhargavi(category) {
  console.log("Category selected:", category);
  if (category === "Non-Veg") {
    displayMenuItems("Non-Veg Starters", nonVegStarter);
  } else if (category === "Veg") {
    displayMenuItems("Veg Starters", vegStarter);
  } else if (category === "sea-food") {
    displayMenuItems("Sea Food", seaFood);
  } else if (category === "main-course") {
    displayMenuItems("Main Course", mainCourse);
  } else if (category === "noodles") {
    displayMenuItems("Noodles", noodles);
  } else if (category === "salads") {
    displayMenuItems("Salads", salads);
  } else if (category === "desserts") {
    displayMenuItems("Desserts", desserts);
  } else if (category === "soups") {
    displayMenuItems("Soups", soups);
  }
}

